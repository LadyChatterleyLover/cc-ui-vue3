<template>
  <div class="cc-search">
    <div class="cc-search-content" :class="{ 'cc-search-round': round }">
      <div class="cc-search-content-label">
        <div v-if="$slots.label">
          <slot name="label"></slot>
        </div>
        <div v-else>{{ label }}</div>
      </div>
      <div class="cc-search-content-cell">
        <div
          class="cc-search-content-icon"
          :class="{
            'cc-search-content-icon-align-center': iconAlign === 'center',
            'cc-search-content-icon-align-right': iconAlign === 'right'
          }"
        >
          <div v-if="$slots.icon">
            <slot name="icon"></slot>
          </div>
          <div v-else>
            <cc-icon :type="icon" color="#323233" v-if="icon"></cc-icon>
            <cc-icon type="search" color="#323233" v-else></cc-icon>
          </div>
        </div>
        <div
          class="cc-search-content-input"
          :class="{
            'cc-search-content-text-align-center': textAlign === 'center',
            'cc-search-content-text-align-right': textAlign === 'right'
          }"
        >
          <input
            class="cc-search-content-input-wrap"
            placeholder-class="cc-search-content-input-placeholder"
            type="text"
            :placeholder="placeholder"
            v-model="inputValue"
            :disabled="disabled"
            :maxlength="maxlength"
            @input="handleInput"
            @focus="handleFocus"
            @blur="handleBlur"
            @confirm="handleConfirm"
          />
          <div
            class="cc-search-content-input-close"
            v-if="inputValue"
            @click="close"
            @mousedown="mousedown"
          >
            <cc-icon color="rgb(96, 98, 102)" type="close" size="14"></cc-icon>
          </div>
        </div>
      </div>
    </div>
    <div class="cc-search-action" @click="cancel">
      <div v-if="$slots.action">
        <slot name="action"></slot>
      </div>
      <div v-else>{{ actionText }}</div>
    </div>
  </div>
</template>

<script lang="ts" setup>
import { defineProps, defineEmits, PropType, ref, watch } from 'vue'

type Align = 'left' | 'center' | 'right'

let props = defineProps({
  value: {
    type: String
  },
  // 左侧内容
  label: {
    type: String,
    default: ''
  },
  // 图标
  icon: {
    type: String,
    default: ''
  },
  // 禁用
  disabled: {
    type: Boolean,
    default: false
  },
  // 是否在搜索框右侧显示取消按钮
  showAction: {
    type: Boolean,
    default: false
  },
  // 取消按钮文字
  actionText: {
    type: String,
    default: '取消'
  },
  // 占位符
  placeholder: {
    type: String,
    default: '请搜索'
  },
  // 是否显示清除图标
  clearable: {
    type: Boolean,
    default: true
  },
  // 是否为圆形搜索框
  round: {
    type: Boolean,
    default: false
  },
  // 最大输入字符
  maxlength: {
    type: [String, Number],
    default: -1,
  },
  // 文字对齐方式
  textAlign: {
    type: String as PropType<Align>,
    defalut: 'left',
  },
  // 图标对齐方式
  iconAlign: {
    type: String as PropType<Align>,
    defalut: 'left',
  }
})
let emits = defineEmits(['update:value', 'input', 'focus', 'blur', 'confirm', 'close', 'cancel'])

let inputValue = ref<string>(props.value!)


let handleInput = (e: Event) => {
  emits('update:value', (e as any).detail.value)
  emits('input', e)
}

let handleFocus = (e: Event) => {
  emits('focus', e)
}
let handleBlur = (e: Event) => {
  emits('blur', e)
}
let handleConfirm = (e: Event) => {
  emits('confirm', e)
}
// 清空输入框时不触发失去焦点事件
let mousedown = (e: Event) => {
  e.preventDefault()
}
// 清空输入框
let close = (e: Event) => {
  inputValue.value = ''
  emits('close', e)
}
// 取消
let cancel = () => {
  if (!props.disabled) emits('cancel')
}

watch(() => inputValue.value, val => {
  emits('update:value', val)
})

</script>

